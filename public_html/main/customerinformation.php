			<h3> NOTICE OF PROPOSED AMENDMENTS TO THE ARIZONA UNIVERSAL SERVICE FUND & PROPOSED SURCHARGE INCREASE DOCKET NO. RT-00000H-97-0137</h3><br>
            <p>On January 13, 2017, the Arizona Corporation Commission ("Commission") issued Decision No. 75919 to begin the process of amending the Arizona 
            Universal Service Fund ("AUSF") rules to facilitate the construction of broadband infrastructure in largely rural areas in Arizona.  The Commission 
            and the Arizona Department of Education are partnering to take advantage of a recent and limited Federal Communications Commission's ("FCC") E-rate 
            program that would allow Arizona to obtain federal matching funds to bridge the broadband &#8220;connectivity gap&#8221; for Arizona&#8217;s rural 
            schools and libraries that do not have adequate internet capabilities.  In 2015, 37 percent of Arizona school districts lacked internet capabilities 
            consistent with the FCC's standard of 100 kilobits per second (0.1 Mbps) per student.  Over 63 percent of these districts were located in rural 
            Arizona.</p> <br>
            <p>Under the FCC's E-rate Modernization Orders, states are able to obtain, on a limited basis, additional benefits from the federal universal 
            service E-rate program for schools and libraries if state matching funds for special construction broadband projects are provided. This program 
            is of limited duration, and the Commission needs to take expedited action in order for schools and libraries in rural Arizona to start obtaining 
            the benefits of this new program.</p><br>
            <p>It is anticipated that this new program under the AUSF will provide a one-time distribution to qualifying Arizona schools and libraries of 
            approximately $8 million in the aggregate. Currently, Arizona customers contribute to the AUSF through a monthly surcharge included in the telephone 
            bills received from the customers' service providers. The adoption of this new program would likely increase the AUSF surcharge on each customer&#8217;s 
            monthly bill.  It is currently estimated that each customer would see an increase of approximately $0.15 per month for approximately one year, if the 
            additional surcharge is collected over a one-year time frame.  The actual amount of the surcharge increase could be higher or lower than this estimate.
            </p><br>
